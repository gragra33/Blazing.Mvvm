namespace Blazing.Common.BEM;

/// <summary>
/// Provides constants for BEM (Block Element Modifier) CSS class naming conventions.
/// </summary>
public class CssClass
{
    /// <summary>
    /// The prefix used for library CSS classes.
    /// </summary>
    public const string LibPrefix = "c-";

    /// <summary>
    /// The separator used to join names in BEM class naming.
    /// </summary>
    public const string NameJoin = "__";

    /// <summary>
    /// The separator used to join modifiers in BEM class naming.
    /// </summary>
    public const string ModifierJoin = "--";
}
